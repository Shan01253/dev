function printChessBoard(){
    var board = [["#","#","#","#","#","#","#","#","#","#"],
                 ["#","#","#","#","#","#","#","#","#","#"],
                 ["#","#","#","#","#","#","#","#","#","#"],
                 ["#","#","#","#","#","#","#","#","#","#"],
                 ["#","#","#","#","#","#","#","#","#","#"],
                 ["#","#","#","#","#","#","#","#","#","#"],
                 ["#","#","#","#","#","#","#","#","#","#"],
                 ["#","#","#","#","#","#","#","#","#","#"],
                 ["#","#","#","#","#","#","#","#","#","#"],
                 ["#","#","#","#","#","#","#","#","#","#"]];
    var n = 10;

    for(var i = 0;i <= n;i++){
        for ( var j = 0; j<=n; j++){
            if(!i%2==0){
                if(!j%2==0){
                    board[i,j] = "B";
                }
                else {
                     board[i,j] = "W";
                }
            } else {
                if(j%2==0){
                    board[i,j] = "W";
                }
                else {
                    board[i,j] = "B";
                }
            
            }
        }
    }
    return board;
}

main();